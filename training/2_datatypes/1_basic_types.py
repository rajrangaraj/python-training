"""
Basic data types in Python: integers, floats, strings, and booleans.
"""

# Integer examples
x = 5
print(f"Integer: {x}, Type: {type(x)}")
print(f"Operations: {x + 1}, {x - 1}, {x * 2}")

# Float examples
pi = 3.14159
print(f"\nFloat: {pi}, Type: {type(pi)}")
print(f"Rounded: {round(pi, 2)}")

# String examples
name = "Python"
print(f"\nString: {name}, Type: {type(name)}")
print(f"Uppercase: {name.upper()}")
print(f"Length: {len(name)}")
print(f"First character: {name[0]}")

# Boolean examples
is_valid = True
print(f"\nBoolean: {is_valid}, Type: {type(is_valid)}")
print(f"Opposite: {not is_valid}")
print(f"Boolean operations: {True and True}, {True or False}") 